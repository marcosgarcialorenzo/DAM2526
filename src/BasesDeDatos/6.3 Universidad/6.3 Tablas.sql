CREATE TABLE ALUMNOS (
DNI VARCHAR2(10) PRIMARY KEY,
NOMBRE VARCHAR2(20),
APELLIDOS VARCHAR2(20),
EDAD 		NUMBER(2)
);

CREATE TABLE ASIGNATURAS (
CODASIG NUMBER(3) PRIMARY KEY,
NOMASIG VARCHAR2(40),
NUMALUMNOS NUMBER(3),
CODCARRERA NUMBER(3)
);

CREATE TABLE CARRERAS (
CODCARRERA NUMBER(3) PRIMARY KEY,
NOMCARRERA VARCHAR2(40),
NUMMATRICULADOS NUMBER(3)
);

CREATE TABLE MATRICULADOS(
DNI VARCHAR2(10),
CODASIG NUMBER(3),
FECHAMATRICULA VARCHAR2(10)
);

