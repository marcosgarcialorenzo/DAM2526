CREATE TABLE PEDIDOS
(
    CODIGO      NUMBER(6) NOT NULL,
    NUMPEDIDO   NUMBER(6),
    FECHAPEDIDO VARCHAR2(10),
    CLIE        NUMBER(5),
    REP         NUMBER(3),
    FAB         VARCHAR2(3),
    PRODUCTO    VARCHAR2(5),
    CAN         NUMBER(3),
    IMPORTE     NUMBER(7),
    CONSTRAINT PK_PEDIDOS PRIMARY KEY (CODIGO)
);

CREATE TABLE CLIENTES
(
    NUMCLIE       NUMBER(5) NOT NULL,
    EMPRESA       VARCHAR2(20),
    REPCLIE       NUMBER(3),
    LIMITECREDITO NUMBER(6),
    CODIGO        NUMBER(6) NOT NULL,
    CONSTRAINT PK_CLIENTES PRIMARY KEY (NUMCLIE),
    CONSTRAINT FK_CLIENTES_PEDIDOS FOREIGN KEY (CODIGO) REFERENCES PEDIDOS (CODIGO)
);

CREATE TABLE EMPLEADOS
(
    NUMEMP   NUMBER(3) NOT NULL,
    NOMBRE   VARCHAR2(20),
    EDAD     NUMBER(2),
    OFICINA  NUMBER(3),
    TITULO   VARCHAR2(20),
    CONTRATO VARCHAR2(10),
    JEFE     NUMBER(3),
    CUOTA    NUMBER(6),
    VENTAS   NUMBER(6),
    CODIGO   NUMBER(6) NOT NULL,
    NUMCLIE  NUMBER(5) NOT NULL,
    CONSTRAINT PK_EMPLEADOS PRIMARY KEY (NUMEMP),
    CONSTRAINT FK_EMPLEADOS_PEDIDOS FOREIGN KEY (CODIGO) REFERENCES PEDIDOS (CODIGO),
    CONSTRAINT FK_EMPLEADOS_CLIENTES FOREIGN KEY (NUMCLIE) REFERENCES CLIENTES (NUMCLIE),
    CONSTRAINT CK_TITULO CHECK (TITULO IN ('REPRESENTANTE', 'DIR. VENTAS', 'DIR. GENERAL'))
);

CREATE TABLE OFICINAS
(
    OFICINA  NUMBER(3) NOT NULL,
    CIUDAD   VARCHAR2(15),
    REGION   VARCHAR2(10),
    DIR      NUMBER(3),
    OBJETIVO NUMBER(6),
    VENTAS   NUMBER(7),
    CONSTRAINT PK_OFICINAS PRIMARY KEY (OFICINA),
    CONSTRAINT CK_REGION CHECK (REGION IN ('CENTRO', 'NORTE', 'ESTE', 'OESTE'))
);

CREATE TABLE PRODUCTOS
(
    IDFAB       VARCHAR2(3) NOT NULL,
    IDPRODUCTO  VARCHAR2(5) NOT NULL,
    DESCRIPCION VARCHAR2(20),
    PRECIO      NUMBER(4),
    EXISTENCIAS NUMBER(3),
    CODIGO      NUMBER(6)   NOT NULL,
    CONSTRAINT PK_PRODUCTOS PRIMARY KEY (IDFAB, IDPRODUCTO),
    CONSTRAINT FK_PRODUCTOS_PEDIDOS FOREIGN KEY (CODIGO) REFERENCES PEDIDOS (CODIGO)
);

